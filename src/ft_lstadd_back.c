#include "../include/libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	while (lst)
	{
		if (!lst->next)
			break ;
		lst = lst->next;
	}
	return (lst);
}

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*l_lst;

	if (!*lst)
	{
		*lst = new;
		return ;
	}
	l_lst = ft_lstlast(*lst);
	l_lst->next = new;
}
